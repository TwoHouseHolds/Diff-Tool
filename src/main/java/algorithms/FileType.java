package algorithms;

import java.util.Arrays;
import java.util.HashSet;
import java.util.Set;

public enum FileType {
    ZIP(0x50, 0x4B, 0x03, 0x04),
    RAR(0x52, 0x61, 0x72, 0x21, 0x1A, 0x07, 0x00),
    PDF(0x25, 0x50, 0x44, 0x46),
    XML(0x3C, 0x3F, 0x78, 0x6D, 0x6C),
    EXE(0x4D, 0x5A),
    JPG(0xFF, 0xD8, 0xFF),
    PNG(0x89, 0x50, 0x4E, 0x47, 0x0D, 0x0A, 0x1A, 0x0A),
    GIF(new int[]{0x47, 0x49, 0x46, 0x38, 0x37, 0x61}, new int[]{0x47, 0x49, 0x46, 0x38, 0x39, 0x61}),
    MP4(new MagicNumberWithOffset(4, 0x66, 0x74, 0x79, 0x70)),
    MP3(new int[]{0xFF, 0xFB}, new int[]{0x49, 0x44, 0x33}),
    WAV(0x52, 0x49, 0x46, 0x46),
    FLAC(0x66, 0x4C, 0x61, 0x43),
    OGG(0x4F, 0x67, 0x67, 0x53),
    MSI(0xD0, 0xCF, 0x11, 0xE0, 0xA1, 0xB1, 0x1A, 0xE1),
    ISO(new MagicNumberWithOffset(0x8001, 0x43, 0x44, 0x30, 0x30, 0x31),
            new MagicNumberWithOffset(0x8801, 0x43, 0x44, 0x30, 0x30, 0x31),
            new MagicNumberWithOffset(0x9001, 0x43, 0x44, 0x30, 0x30, 0x31),
            new MagicNumberWithOffset(0, 0x45, 0x4D, 0x55, 0x33)),
    DEB(new int[]{0x21, 0x3C, 0x61, 0x72, 0x63, 0x68, 0x3E, 0x0A}, new int[]{0x6B, 0x6F, 0x6C, 0x79, 0x6D, 0x61, 0x63}),
    // TODO
    DMG(0x78, 0x01, 0x73, 0x0D, 0x62, 0x62, 0x60),
    TEXT(-1),
    BINARY(-1),
    ERROR(-1);

    public final int[] magicNumbers;
    public final Set<int[]> alternativeMagicNumbers = new HashSet<>();
    public final Set<MagicNumberWithOffset> magicNumbersWithOffset = new HashSet<>();

    FileType(int... magicNumbers) {
        this.magicNumbers = magicNumbers;
    }

    FileType(int[] mainMagicNumbers, int[]... alternatives) {
        magicNumbers = mainMagicNumbers;
        alternativeMagicNumbers.addAll(Arrays.asList(alternatives));
    }

    public record MagicNumberWithOffset(int offset, int... magicNumber){}

    FileType(MagicNumberWithOffset... offsetMagicNumbers) {
        magicNumbers = null;
        magicNumbersWithOffset.addAll(Arrays.asList(offsetMagicNumbers));
    }
}
