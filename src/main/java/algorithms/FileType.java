package algorithms;

import java.util.Arrays;
import java.util.HashSet;
import java.util.Set;

public enum FileType {
    ZIP(new int[]{0x50, 0x4B, 0x03, 0x04}, new int[]{0x50, 0x4B, 0x05, 0x06}, new int[]{0x50, 0x4B, 0x07, 0x08}), // (comments to fix auto-formating)
    RAR(new int[]{0x52, 0x61, 0x72, 0x21, 0x1A, 0x07, 0x00}, new int[]{0x52, 0x61, 0x72, 0x21, 0x1A, 0x07, 0x01, 0x00}), //
    PDF(0x25, 0x50, 0x44, 0x46, 0x2D), //
    EXE(new int[]{0x4D, 0x5A}, new int[]{0x5A, 0x4D}), //
    JPG(0xFF, 0xD8, 0xFF), //
    PNG(0x89, 0x50, 0x4E, 0x47, 0x0D, 0x0A, 0x1A, 0x0A), //
    GIF(new int[]{0x47, 0x49, 0x46, 0x38, 0x37, 0x61}, new int[]{0x47, 0x49, 0x46, 0x38, 0x39, 0x61}), //
    MP4(new MagicNumberWithOffset(4, true, 0x66, 0x74, 0x79, 0x70)), //
    MP3(new int[]{0xFF, 0xFB}, new int[]{0xFF, 0xF3}, new int[]{0xFF, 0xF2}, new int[]{0x49, 0x44, 0x33}), //
    WAV(new int[]{0x52, 0x49, 0x46, 0x46}, new int[]{0x57, 0x41, 0x56, 0x45}), //
    FLAC(0x66, 0x4C, 0x61, 0x43), //
    OGG(0x4F, 0x67, 0x67, 0x53), //
    MSI(0xD0, 0xCF, 0x11, 0xE0, 0xA1, 0xB1, 0x1A, 0xE1), //
    ISO(new MagicNumberWithOffset(0x8001, true, 0x43, 0x44, 0x30, 0x30, 0x31), //
            new MagicNumberWithOffset(0x8801, true, 0x43, 0x44, 0x30, 0x30, 0x31), //
            new MagicNumberWithOffset(0x9001, true, 0x43, 0x44, 0x30, 0x30, 0x31), //
            new MagicNumberWithOffset(0, true, 0x45, 0x4D, 0x55, 0x33)), //
    DEB(0x21, 0x3C, 0x61, 0x72, 0x63, 0x68, 0x3E, 0x0A), //
    DMG(new MagicNumberWithOffset(512, false, 0x6B, 0x6F, 0x6C, 0x79)), //
    SEVENZIP(0x37, 0x7A, 0xBC, 0xAF, 0x27, 0x1C),
    TEXT(-1), //
    BINARY(-1), //
    ERROR(-1);

    public final int[] magicNumbers;
    public final Set<int[]> alternativeMagicNumbers = new HashSet<>();
    public final Set<MagicNumberWithOffset> magicNumbersWithOffset = new HashSet<>();

    FileType(int... magicNumbers) {
        this.magicNumbers = magicNumbers;
    }

    FileType(int[] mainMagicNumbers, int[]... alternatives) {
        magicNumbers = mainMagicNumbers;
        alternativeMagicNumbers.addAll(Arrays.asList(alternatives));
    }

    FileType(MagicNumberWithOffset... offsetMagicNumbers) {
        magicNumbers = null;
        magicNumbersWithOffset.addAll(Arrays.asList(offsetMagicNumbers));
    }

    public record MagicNumberWithOffset(int offset, boolean offsetFromLeft, int... magicNumber) {
    } // if offsetFromLeft is false, the offset is applied at the end/right of the file, e.g. the offset of .dmg files is "end â€“ 512"

}